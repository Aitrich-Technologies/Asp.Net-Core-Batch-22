CREATE FUNCTION GETEMPLOYEE_DEPT(@DEPT_NAME VARCHAR(50))
RETURNS @CUSTOMERS_NAMES TABLE
(NAME VARCHAR(25))

AS

BEGIN
	INSERT INTO @CUSTOMERS_NAMES
	
	SELECT 
		Name
	FROM 
		EmployeeSalary 
	WHERE 
		Department=@DEPT_NAME
RETURN
END


