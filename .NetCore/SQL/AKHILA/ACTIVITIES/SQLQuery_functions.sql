CREATE FUNCTION GET_ORDERS(@AMNT INT)
RETURNS @ORDER_IDS TABLE
	(ORDER_ID INT)
AS
BEGIN
	INSERT INTO @ORDER_IDS
	SELECT
		OrderID 
	FROM
		Orders
	WHERE 
		Amount>@AMNT
RETURN 
END

